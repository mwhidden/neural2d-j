package neural2d;

/**
 * @author Michael C. Whidden
 */
public class Neural2DException extends Exception
{

    public Neural2DException(String message)
    {
        super(message);
    }

    public Neural2DException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public Neural2DException(Throwable cause)
    {
        super(cause);
    }

}
